package Selektion;

public class Switchcase {

public static void main(String[] args) {



	int val=34;


	switch(val) {

	case 1:
		System.out.println("val1");

		break;

	case 2:
		System.out.println("val2");
		break;

	case 3:
		System.out.println("val3");
		break;

	default:

		System.out.println("du har valt något annat");
	}


}
}
